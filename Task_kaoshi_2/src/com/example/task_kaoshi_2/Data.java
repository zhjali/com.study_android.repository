package com.example.task_kaoshi_2;

public class Data {
	String name;
	String url;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Data(String name, String url) {
		super();
		this.name = name;
		this.url = url;
	}
	

}
