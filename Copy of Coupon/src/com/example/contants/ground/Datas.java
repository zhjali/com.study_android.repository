package com.example.contants.ground;

public class Datas {
	private String icon;
	private String flag;

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	@Override
	public String toString() {
		return "Datas [icon=" + icon + ", flag=" + flag + "]";
	}

}
